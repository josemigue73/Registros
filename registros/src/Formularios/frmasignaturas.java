    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
        if (txtCod_Asignatura.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un Codigo para La Asignatura");
            txtCod_Asignatura.requestFocus();
            return;
        }
        if (txtNom_Asignatura.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar El Nombre para la Asignatura");
            txtNom_Asignatura.requestFocus();
            return;
        }
          if (intensidad.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar La Intensidad Horaria para la Asignatura");
            intensidad.requestFocus();
            return;
        }           

        vasignaturas dts = new vasignaturas();
        fasignaturas func = new fasignaturas();

        
        dts.setCod_Asignatura(txtCod_Asignatura.getText());
        dts.setNom_Asignatura(txtNom_Asignatura.getText());   
        dts.setInt_Horaria(Integer.parseInt(intensidad.getText()));   
       
        int seleccionado = cboGrados.getSelectedIndex();
        dts.setGrado((String) cboGrados.getItemAt(seleccionado));

        
        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "La Asignatura fue registrado satisfactoriamente");
                mostrar("");
                inhabilitar();

            }

        }
        else if (accion.equals("editar")){
            dts.setIdAsignatura(Integer.parseInt(txtidAsignatura.getText()));
            
            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "La Asignatura fue Editado Satisfactoriamente");
                mostrar("");
                inhabilitar();
            }
        }

    }//GEN-LAST:event_btnguardarActionPerformed

