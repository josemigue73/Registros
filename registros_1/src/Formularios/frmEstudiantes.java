    private void tablalistadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablalistadoMouseClicked
        // TODO add your handling code here:
       
        btnguardar.setText("Editar");
        habilitar();
        btneliminar.setEnabled(true);
        accion="editar";

        int fila = tablalistado.rowAtPoint(evt.getPoint());

        txtCod_Estudiantes.setText(tablalistado.getValueAt(fila, 0).toString());
        Tipo.setSelectedItem(tablalistado.getValueAt(fila, 1).toString());
        txtnum_documento.setText(tablalistado.getValueAt(fila, 2).toString());
        txtNombres.setText(tablalistado.getValueAt(fila, 3).toString());
        txtPrimer_Apellido.setText(tablalistado.getValueAt(fila, 4).toString());           
        txtSegundo_Apellido.setText(tablalistado.getValueAt(fila, 5).toString());
        txtTelefono.setText(tablalistado.getValueAt(fila, 6).toString());
        txtDireccion.setText(tablalistado.getValueAt(fila, 7).toString());
        txtAño_Ingreso.setText(tablalistado.getValueAt(fila,8).toString());
        jFecha_Nacimiento.setText(tablalistado.getValueAt(fila, 9).toString());
       
    }//GEN-LAST:event_tablalistadoMouseClicked

