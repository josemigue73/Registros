    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
        
          if (txtnum_documento.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un Numero de documento");
            txtnum_documento.requestFocus();
            return;
        }
       if (txtNombres.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un Nombre para el producto");
            txtNombres.requestFocus();
            return;
        }
        
        if (txtPrimer_Apellido.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un precio diario para para la venta del producto");
            txtPrimer_Apellido.requestFocus();
            return;
        }

       if (txtSegundo_Apellido.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un precio diario para para la venta del producto");
            txtSegundo_Apellido.requestFocus();
            return;
        }
        if (txtTelefono.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un Telefono");
            txtTelefono.requestFocus();
            return;
        }
         if (txtDireccion.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un precio diario para para la venta del producto");
            txtDireccion.requestFocus();
            return;
        }
        if (txtAño_Ingreso.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes Digitar el año academico del Estudiante");
            txtAño_Ingreso.requestFocus();
            return;
        } 
        
         
        
                  
                
        vestudiantes dts = new vestudiantes();
        fEstudiante func = new fEstudiante();
        
       
        dts.setNum_Documento(txtnum_documento.getText());
        dts.setNombres(txtNombres.getText());
        dts.setP_apellido(txtPrimer_Apellido.getText());
        dts.setS_apellido(txtSegundo_Apellido.getText());
        dts.setTelefono(txtTelefono.getText());
        dts.setDireccion(txtDireccion.getText());
        dts.setAño_Ingreso(txtAño_Ingreso.getText());
        
        
        int seleccionado = Tipo.getSelectedIndex();
         dts.setTipo_Documento(Tipo.getItemAt(seleccionado));
        
         //Calendar cal;
        //int d,m,a;
        //cal=jFecha_Nacimiento.getCalendar();
        //d=cal.get(Calendar.DAY_OF_MONTH);
        //m=cal.get(Calendar.MONTH);
        //a=cal.get(Calendar.YEAR) - 1900;
        //dts.setFecha_nacimiento(new Date(a,m,d));
        
        
                
         
        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "el Estudiante fue ingresado satisfactoriamente");
                mostrar("");
                inhabilitar();

            }

        }
        else if (accion.equals("editar")){
            dts.setCod_Estudiantes(Integer.parseInt(txtCod_Estudiantes.getText()));

            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El producto fue Editado satisfactoriamente");
                mostrar("");
                inhabilitar();
            }
        }
 
        
        
        
        
          
        

        
    }//GEN-LAST:event_btnguardarActionPerformed

